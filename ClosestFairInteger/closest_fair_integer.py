import collections

def is_fair(num):
    """
    Checks if an integer is 'fair'. A fair integer is one where all its digits
    appear an even number of times.
    """
    s = str(num)
    counts = collections.defaultdict(int)
    for digit_char in s:
        counts[digit_char] += 1
    
    for count in counts.values():
        if count % 2 != 0:
            return False
    return True

def closestFairInteger(N):
    """
    Finds the closest fair integer to N. If there are two fair integers
    equidistant from N, the smaller one is returned.
    """
    diff = 0
    while True:
        # Check N - diff
        candidate_minus = N - diff
        # Fair integers are typically non-negative.
        # 0 is not fair (digit '0' appears once). Smallest fair numbers are 11, 22, etc.
        if candidate_minus >= 0 and is_fair(candidate_minus):
            return candidate_minus
        
        # Check N + diff
        # Only check N + diff if diff > 0, as N itself was covered by candidate_minus when diff=0.
        if diff > 0:
            candidate_plus = N + diff
            if is_fair(candidate_plus):
                return candidate_plus
        
        diff += 1

